"""
Перечисления (enum) домена CRM.

Это единственный источник правды для значений status / source / type.
Используются одновременно:
    - моделями SQLAlchemy (значения уходят в CHECK-констрейнты БД);
    - Pydantic-схемами (валидация входных данных API);
    - сервисным слоем (логика переходов воронки).

Хранение в БД: поле типа String + CHECK-констрейнт со списком значений.
Нативный PostgreSQL ENUM-тип сознательно не используется - воронка лидов
будет эволюционировать (вставка статусов в середину, переименования),
а нативный enum плохо переживает такие миграции. CHECK-констрейнт меняется
одной обратимой операцией DROP/ADD CONSTRAINT.

Наследование от str делает членов enum полноценными строками: значение
сериализуется в JSON как строка, сравнивается со строкой напрямую и
пишется в БД без явного .value.
"""

from enum import Enum


class LeadStatus(str, Enum):
    """
    Статус лида в воронке продаж.

    Линейный путь:
        NEW -> CONTACTED -> TRIAL_SCHEDULED -> TRIAL_ATTENDED -> CONVERTED

    LOST - терминальный статус, достижимый с любого этапа (с указанием
    причины в Lead.lost_reason). Это не часть линейной цепочки, а выход
    из воронки.

    Движение по статусам в MVP полностью ручное: админ переключает статус
    через PATCH-эндпоинт. Автоматических переходов (например, по факту
    проведённого занятия) нет - CRM не знает о расписании.
    """

    NEW = "new"
    CONTACTED = "contacted"
    TRIAL_SCHEDULED = "trial_scheduled"
    TRIAL_ATTENDED = "trial_attended"
    CONVERTED = "converted"
    LOST = "lost"


class LeadSource(str, Enum):
    """
    Источник заявки - откуда пришёл лид.

    LANDING - публичная форма на лендинге (основной поток).
    INSTAGRAM, REFERRAL - заполняется при ручном создании лида админом.
    MANUAL - лид заведён админом вручную без конкретного канала.
    """

    LANDING = "landing"
    INSTAGRAM = "instagram"
    REFERRAL = "referral"
    MANUAL = "manual"


class LeadActivityType(str, Enum):
    """
    Тип записи в журнале активностей лида.

    NOTE - свободная текстовая заметка админа.
    CALL - отметка о звонке (с описанием в content).
    STATUS_CHANGED - системная запись о смене статуса воронки;
                     создаётся автоматически, created_by при этом - админ,
                     инициировавший смену.
    """

    NOTE = "note"
    CALL = "call"
    STATUS_CHANGED = "status_changed"